  <h3>About:</h3>
    <p>This project was developed by the 2023 Computer Science majors of Loras College, and it was proposed to the team by the graduate student Mason McMillen. Mason recognised a lack of tools available to the wrestling community that allowed them to analyze meets and matches, so he thought of a way for teams to have those tools. He wanted to have an app developed where teams could take down information from a match in real time then get meaningful statistics from the data they collected.</p>
  <p>Based on what Mason wanted we created Wrincle (WRestling INformation CoLlection and Evaluation). Wrincle is a project that involves collecting wrestling data in real time, then sending that data to a web server to store the data for later use. To collect data, we are developing a phone app that takes in information from a coach or assistant at a wrestling meet, then after each match the data is sent in a file to the web server. From there the data is automatically sorted and inserted into a database for future use. Then any time during the season a coach or player can come to the website and generate statistics on the team as a whole or an individual wrestler from the data that was collected throughout the season.</p>
